#include "b_printf.h"

int	ft_ps(char *str)
{
	int i;
	int count;

	i = 0;
	count = 0;
	if (!str)
		return (ft_ps("(null)"));
	else
	{
		while (str[i])
		{
			ft_pc(str[i]);
			i++;
			count++;
		}
		return (count);
	}
}
